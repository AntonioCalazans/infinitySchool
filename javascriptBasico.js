// Definindo as notas e pesos
var n1 = 8;  // substitua com a primeira nota
var n2 = 7;  // substitua com a segunda nota
var n3 = 9;  // substitua com a terceira nota

var p1 = 0.3;  // substitua com o peso da primeira nota
var p2 = 0.4;  // substitua com o peso da segunda nota
var p3 = 0.3;  // substitua com o peso da terceira nota

// Calculando a média ponderada
var media = (n1 * p1 + n2 * p2 + n3 * p3) / (p1 + p2 + p3);

// Exibindo a média ponderada no console
console.log("A média ponderada é: " + media);
